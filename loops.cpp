        // triangle 1
        int col = 1;
        while (col <= n - row + 1)
        {
            cout << col;
            col = col + 1;
        }
        // triangle 2

        int start = row - 1;
        while (start)
        {
            cout << "*";
            start = start - 1;
        }

        // triangle 3
        int space = row - 1;
        while (space)
        {
            cout << "*";
            space = space - 1;
        }

        // triangle 4

        int col1 = 1;

        while (col1 <= (n - row + 1))
        {
            cout << (n - row + 1) - col1 + 1;
            col1 = col1 + 1;
        }

        cout << endl;
